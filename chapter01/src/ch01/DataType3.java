package ch01;

public class DataType3 {
//코드의 시작점
	public static void main(String[] args) {
		
		// 실수형 2가지
		// 1.float - 4byte
		// 2.double -8byte
		
		//실수형 단위를 다룰때 R value 기본 연산 단위는 double이다.
		// float 보다 double이 더 정확하기 때문에 기본 연산 단위를 double로 하였다.
		float a = 0.5F; // ( 접미사 F를 붙여야함 )
		float b = 12.9f;// (접미사 대소문자 구문 없음)
		
		double c = 0.12345;
		double d = 0.55; //8바이트
		
	}// end of main
	
}// end of class
